/*14. Desenvolva um algoritmo que mostre os valores da série abaixo.
● Série: 0, 5, 10, 15, 20, 25, 30, 35, 40, 45, 50*/
public class Questao14 {
    public static void main(String[] args) {
        for (int i = 0; i <= 50; i += 5) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}
